#include <stdio.h>
int	tolower(int	c)
{
	if (c >= 65 && c <= 90)
	{
		c = c + 32;
		return (c);
	}
	else
		return (c);
}

int main()
{
	char tf = 'Y';
	printf("%c",tolower(tf));
}
